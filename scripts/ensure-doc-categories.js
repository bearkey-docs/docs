const fs = require('fs');
const path = require('path');

const siteDir = path.join(__dirname, '..');
const docsRoots = [
  path.join(siteDir, 'docs_cn'),
  ...getLocalizedDocsRoots(),
];

function getLocalizedDocsRoots() {
  const i18nDir = path.join(siteDir, 'docs_en');

  if (!fs.existsSync(i18nDir)) {
    return [];
  }

  return fs
    .readdirSync(i18nDir, { withFileTypes: true })
    .filter((entry) => entry.isDirectory() && !entry.name.startsWith('.'))
    .map((entry) =>
      path.join(
        i18nDir,
        entry.name,
        'docusaurus-plugin-content-docs',
        'current',
      ),
    )
    .filter((docsRoot) => fs.existsSync(docsRoot));
}

function titleFromDirName(dirName) {
  return dirName
    .replace(/[-_]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function hasDocContent(dirPath) {
  return fs.readdirSync(dirPath, { withFileTypes: true }).some((entry) => {
    if (entry.name.startsWith('.')) {
      return false;
    }

    const entryPath = path.join(dirPath, entry.name);

    if (entry.isDirectory()) {
      return hasDocContent(entryPath);
    }

    return entry.isFile() && /\.(md|mdx)$/i.test(entry.name);
  });
}

function writeCategoryFile(categoryPath, category) {
  fs.writeFileSync(categoryPath, `${JSON.stringify(category, null, 2)}\n`);
}

function readCategoryFile(categoryPath) {
  return JSON.parse(fs.readFileSync(categoryPath, 'utf8'));
}

function hasSameNameDoc(dirPath) {
  const dirName = path.basename(dirPath);

  return ['.md', '.mdx'].some((extension) =>
    fs.existsSync(path.join(dirPath, `${dirName}${extension}`)),
  );
}

function hasReadmeDoc(dirPath) {
  return ['README.md', 'README.mdx'].some((fileName) =>
    fs.existsSync(path.join(dirPath, fileName)),
  );
}

function ensureSameNameDocVisible(dirPath) {
  const categoryPath = path.join(dirPath, '_category_.json');

  if (!fs.existsSync(categoryPath) || !hasSameNameDoc(dirPath)) {
    return;
  }

  const category = readCategoryFile(categoryPath);

  if (Object.prototype.hasOwnProperty.call(category, 'link')) {
    return;
  }

  category.link = null;
  writeCategoryFile(categoryPath, category);
  console.log(`Updated ${path.relative(path.join(__dirname, '..'), categoryPath)}`);
}

function ensureCategoryFile(dirPath) {
  const categoryPath = path.join(dirPath, '_category_.json');

  if (hasReadmeDoc(dirPath)) {
    return;
  }

  if (fs.existsSync(categoryPath)) {
    ensureSameNameDocVisible(dirPath);
    return;
  }

  if (!hasDocContent(dirPath)) {
    return;
  }

  const label = titleFromDirName(path.basename(dirPath));
  const category = {
    label,
    position: 100,
    collapsed: false,
  };

  writeCategoryFile(categoryPath, category);
  console.log(`Created ${path.relative(path.join(__dirname, '..'), categoryPath)}`);
  ensureSameNameDocVisible(dirPath);
}

function walk(dirPath) {
  for (const entry of fs.readdirSync(dirPath, { withFileTypes: true })) {
    if (!entry.isDirectory() || entry.name.startsWith('.')) {
      continue;
    }

    const childPath = path.join(dirPath, entry.name);
    ensureCategoryFile(childPath);
    walk(childPath);
  }
}

for (const docsRoot of docsRoots) {
  if (!fs.existsSync(docsRoot)) {
    throw new Error(`Docs directory not found: ${docsRoot}`);
  }

  walk(docsRoot);
}


function getDocIdFromFile(docsRoot, filePath) {
  const relativePath = path.relative(docsRoot, filePath).replace(/\\/g, '/');
  const parsedPath = path.parse(relativePath);
  const docId = path.join(parsedPath.dir, parsedPath.name).replace(/\\/g, '/');

  return docId.endsWith('/index') ? docId.slice(0, -'/index'.length) : docId;
}

function collectDocIds(docsRoot) {
  const docIds = [];

  function collect(dirPath) {
    for (const entry of fs.readdirSync(dirPath, { withFileTypes: true })) {
      if (entry.name.startsWith('.') || entry.name === '_category_.json') {
        continue;
      }

      const entryPath = path.join(dirPath, entry.name);

      if (entry.isDirectory()) {
        collect(entryPath);
        continue;
      }

      if (entry.isFile() && /\.(md|mdx)$/i.test(entry.name)) {
        docIds.push(getDocIdFromFile(docsRoot, entryPath));
      }
    }
  }

  collect(docsRoot);
  return [...new Set(docIds)].sort();
}

function writeEnglishDocPaths() {
  const englishDocsRoot = path.join(
    siteDir,
    'docs_en',
    'en',
    'docusaurus-plugin-content-docs',
    'current',
  );
  const generatedDir = path.join(siteDir, 'src', 'generated');
  const generatedPath = path.join(generatedDir, 'englishDocPaths.js');
  const docIds = fs.existsSync(englishDocsRoot)
    ? collectDocIds(englishDocsRoot)
    : [];
  const source = [
    '// This file is generated by scripts/ensure-doc-categories.js.',
    '// Do not edit it manually.',
    `export const ENGLISH_DOC_PATHS = ${JSON.stringify(docIds, null, 2)};`,
    '',
  ].join('\n');

  fs.mkdirSync(generatedDir, { recursive: true });
  fs.writeFileSync(generatedPath, source);
}

writeEnglishDocPaths();
